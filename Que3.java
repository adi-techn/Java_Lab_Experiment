public class Que3{
     public static void main(String[] args) {
         System.out.print("ASCII value for 0-9::");
         for(int i='0';i<='9';i++){
             System.out.print(i+" ");
         }
         System.out.println();
         System.out.println("ASCII value of characters::");
         for(char i='!';i<='$';i++){
             System.out.println(i+":"+(int)i);
         }
     }
 }
 
 